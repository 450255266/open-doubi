package com.lovely.devpos.pojo;

public class SVNPojo {

	private String optinoalSvnUrl;// 操作路径

	private String userName;// 操作用户

	private String password;// 操作密码

	private String version;// 操作版本

	private String serviceFileUrl;// 服务文件路径

	public String getOptinoalSvnUrl() {
		return optinoalSvnUrl;
	}

	public void setOptinoalSvnUrl(String optinoalSvnUrl) {
		this.optinoalSvnUrl = optinoalSvnUrl;
	}

	public String getUserName() {
		return userName;
	}

	public void setUserName(String userName) {
		this.userName = userName;
	}

	public String getPassword() {
		return password;
	}

	public void setPassword(String password) {
		this.password = password;
	}

	public String getVersion() {
		return version;
	}

	public void setVersion(String version) {
		this.version = version;
	}

	public String getServiceFileUrl() {
		return serviceFileUrl;
	}

	public void setServiceFileUrl(String serviceFileUrl) {
		this.serviceFileUrl = serviceFileUrl;
	}
	
}
